-- ===========================
--    TEMPORAL DATATYPES
-- ===========================

-- In MySQL, there are several temporal data types that can be used to store and
-- manipulate time and date values. These include:
-- 1.DATE - used for storing date values in the format YYYY-MM-DD
-- 2.TIME - used for storing time values in the format HH:MMSS.
-- 3.DATETIME - used for storing date and time values in the format YYYY•MM•DD
-- 4.TIMESTAMP - used for storing date and time values in the format YYYY-MM-DD It has a range of 1970-01-01 00:00:01 UTC to UTC
-- 5. YEAR - used for storing year values in 2-digit or a-digit format (YYYY or YY).If the year is specified with 2 digits, it is assumed to be in the range
-- 1910-2069 (inclusive).


